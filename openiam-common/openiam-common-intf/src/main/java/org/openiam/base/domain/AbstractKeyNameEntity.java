package org.openiam.base.domain;

import javax.persistence.MappedSuperclass;

@MappedSuperclass
public abstract class AbstractKeyNameEntity extends KeyEntity {

	protected String name;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		AbstractKeyNameEntity other = (AbstractKeyNameEntity) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return String.format("AbstractKeyNameEntity [name=%s, toString()=%s]",
				name, super.toString());
	}

	
}
